CREATE TABLE IF NOT EXISTS temples (
    id                    SERIAL PRIMARY KEY,
    name                  VARCHAR(255) NOT NULL,
    alternate_name        VARCHAR(255),
    deity                 VARCHAR(255),
    other_deities         TEXT[],
    category              VARCHAR(100),
    location_city         VARCHAR(100),
    location_state        VARCHAR(100),
    full_address          TEXT,
    latitude              NUMERIC(10,7),
    longitude             NUMERIC(10,7),
    timings_general       VARCHAR(255),
    timings_morning_aarti VARCHAR(255),
    timings_evening_aarti VARCHAR(255),
    timings_closed_on     VARCHAR(255),
    entry_fee             VARCHAR(100),
    dress_code            VARCHAR(255),
    special_darshan       TEXT,
    famous_for            TEXT,
    history               TEXT,
    best_time_visit       VARCHAR(255),
    festivals             TEXT[],
    contact_phone         VARCHAR(50),
    website               VARCHAR(255),
    image_url             TEXT,
    tag                   VARCHAR(100),
    rating                NUMERIC(3,1),
    reviews               INTEGER DEFAULT 0,
    pincode               VARCHAR(10),
    nearest_railway       VARCHAR(255),
    nearest_airport       VARCHAR(255),
    is_active             BOOLEAN DEFAULT TRUE,
    created_at            TIMESTAMP DEFAULT NOW(),
    updated_at            TIMESTAMP DEFAULT NOW()
);

CREATE UNIQUE INDEX IF NOT EXISTS uq_temples_name_city ON temples (name, location_city);

